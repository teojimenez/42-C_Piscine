int	ft_str_is_numeric(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 47 && str[i] < 58)
			c = 1;
		else
			return (0);
		i++;
	}
	if (str[i] == '\0')
		c = 1;
	return (c);
}
